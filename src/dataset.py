"""
dataset.py -- FibreDataset
=========================
Loads the synthetic fibre dataset generated by generate_fibre_dataset.py
and returns samples in the format expected by torchvision's Mask R-CNN:

  image : Float[3, H, W]  in [0, 1]
  target: {
      boxes   : Float[N, 4]   -- xyxy format
      labels  : Int64[N]      -- all 1 (fibre class)
      masks   : Bool[N, H, W] -- one binary mask per instance
      image_id: Int64[1]
      area    : Float[N]
      iscrowd : Int64[N]      -- all 0
  }

The instance masks are decoded from the RGB instance mask PNG using the
colour lookup stored in manifest.json.
"""

import json
import os

import numpy as np
import torch
from PIL import Image
from torch.utils.data import Dataset
import torchvision.transforms.functional as TF
import albumentations as A
from albumentations.pytorch import ToTensorV2


# --- Augmentation pipelines ---------------------------------------------------

def get_train_transforms(image_size):
    sz = (image_size, image_size)
    return A.Compose([
        A.RandomResizedCrop(size=sz, scale=(0.7, 1.0), p=1.0),
        A.HorizontalFlip(p=0.5),
        A.VerticalFlip(p=0.5),
        A.RandomRotate90(p=0.5),
        A.ColorJitter(brightness=0.3, contrast=0.3, saturation=0.2, hue=0.05, p=0.7),
        A.GaussNoise(var_limit=(5, 25), p=0.4),
        A.GaussianBlur(blur_limit=(3, 5), p=0.3),
        A.Normalize(mean=(0.485, 0.456, 0.406), std=(0.229, 0.224, 0.225)),
        ToTensorV2(),
    ])


def get_val_transforms(image_size):
    return A.Compose([
        A.Resize(height=image_size, width=image_size),
        A.Normalize(mean=(0.485, 0.456, 0.406), std=(0.229, 0.224, 0.225)),
        ToTensorV2(),
    ])


# --- Dataset ------------------------------------------------------------------

class FibreDataset(Dataset):
    """
    Args:
        root      : path to the fibre_dataset directory
        split     : "train", "val", or "test"
        transforms: albumentations transform pipeline (or None)
        min_area  : skip instances whose mask area < this many pixels
    """

    def __init__(self, root, split="train", transforms=None, min_area=16):
        self.root       = root
        self.split      = split
        self.transforms = transforms
        self.min_area   = min_area

        manifest_path = os.path.join(root, "manifest.json")
        with open(manifest_path) as f:
            manifest = json.load(f)

        self.samples = [s for s in manifest["samples"] if s["split"] == split]
        if len(self.samples) == 0:
            raise ValueError(f"No samples found for split '{split}' in {manifest_path}")

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        sample = self.samples[idx]

        # -- Load image --
        img_path  = os.path.join(self.root, sample["image"])
        mask_path = os.path.join(self.root, sample["mask"])

        image     = np.array(Image.open(img_path).convert("RGB"), dtype=np.uint8)
        mask_rgb  = np.array(Image.open(mask_path).convert("RGB"), dtype=np.uint8)

        H, W = image.shape[:2]

        # -- Decode instance masks from colour lookup --
        instance_masks = []
        valid_fibres   = []

        for fibre in sample["fibres"]:
            rgb   = tuple(fibre["mask_rgb"])
            inst_mask = np.all(mask_rgb == np.array(rgb, dtype=np.uint8), axis=-1)  # HxW bool
            area  = inst_mask.sum()
            if area < self.min_area:
                continue
            instance_masks.append(inst_mask.astype(np.uint8))
            valid_fibres.append(fibre)

        if len(instance_masks) == 0:
            # Return empty target (edge case -- very few pixels visible)
            image_t = TF.to_tensor(Image.fromarray(image))
            target  = {
                "boxes":    torch.zeros((0, 4), dtype=torch.float32),
                "labels":   torch.zeros(0, dtype=torch.int64),
                "masks":    torch.zeros((0, H, W), dtype=torch.uint8),
                "image_id": torch.tensor([idx]),
                "area":     torch.zeros(0, dtype=torch.float32),
                "iscrowd":  torch.zeros(0, dtype=torch.int64),
            }
            return image_t, target

        masks_arr = np.stack(instance_masks, axis=0)  # NxHxW uint8

        # -- Apply augmentations --
        if self.transforms is not None:
            # albumentations expects masks as HxWxN
            masks_hwn = masks_arr.transpose(1, 2, 0)
            aug = self.transforms(
                image=image,
                masks=[masks_hwn[:, :, i] for i in range(masks_hwn.shape[2])]
            )
            image    = aug["image"]          # tensor CxHxW (normalised)
            aug_masks = aug["masks"]         # list of HxW uint8 arrays
            masks_arr = np.stack(aug_masks, axis=0)  # NxHxW
        else:
            image = TF.to_tensor(Image.fromarray(image))

        # -- Build bounding boxes from masks --
        boxes = []
        keep  = []
        for i, m in enumerate(masks_arr):
            rows = np.any(m, axis=1)
            cols = np.any(m, axis=0)
            if not rows.any():
                continue
            y0, y1 = np.where(rows)[0][[0, -1]]
            x0, x1 = np.where(cols)[0][[0, -1]]
            if x1 <= x0 or y1 <= y0:
                continue
            boxes.append([x0, y0, x1, y1])
            keep.append(i)

        if len(keep) == 0:
            target = {
                "boxes":    torch.zeros((0, 4), dtype=torch.float32),
                "labels":   torch.zeros(0, dtype=torch.int64),
                "masks":    torch.zeros((0, *masks_arr.shape[1:]), dtype=torch.uint8),
                "image_id": torch.tensor([idx]),
                "area":     torch.zeros(0, dtype=torch.float32),
                "iscrowd":  torch.zeros(0, dtype=torch.int64),
            }
            return image, target

        masks_arr = masks_arr[keep]
        boxes_t   = torch.as_tensor(boxes, dtype=torch.float32)
        areas     = (boxes_t[:, 3] - boxes_t[:, 1]) * (boxes_t[:, 2] - boxes_t[:, 0])

        target = {
            "boxes":    boxes_t,
            "labels":   torch.ones(len(keep), dtype=torch.int64),   # class 1 = fibre
            "masks":    torch.as_tensor(masks_arr, dtype=torch.uint8),
            "image_id": torch.tensor([idx]),
            "area":     areas,
            "iscrowd":  torch.zeros(len(keep), dtype=torch.int64),
        }

        return image, target


# --- Collate fn ---------------------------------------------------------------

def collate_fn(batch):
    """Mask R-CNN expects a list of (image, target) tuples, not stacked tensors."""
    return tuple(zip(*batch))
